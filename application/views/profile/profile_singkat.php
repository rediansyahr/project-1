							<div class="detail_text">
							Balai Besar Pengembangan Latihan Kerja (BBPLK) Bekasi lebih dikenal dengan nama CEVEST yang merupakan kepanjangan dari Centre for Vocational and Extention Service Training. CEVEST didirikan pada tahun 1985 dengan bantuan dari pemerintah Jepang sebagai bentuk kerjasama dalam rangka pengembangan Sumber Daya Manusia (SDM) di kawasan ASEAN. CEVEST diresmikan oleh Perdana Menteri Jepang, Zenko Suzuki, sementara dari Indonesia diwakili oleh Sudomo selaku Menteri Tenaga Kerja dan Hartanto sebagai Menteri Perindustrian. 
							<br><br>
							Lembaga pengembangan pelatihan kerja ini sempat mengalami beberapa kali perubahan nama. Mulai dari Diklat Instruktur CEVEST pada tahun 1986, kemudian Balai Latihan Instruktur dan Pengembangan (BLIP) pada tahun 1990, hingga menjadi Pusat Pengembangan Pelatihan Tenaga Kerja Industri Jasa (P3TKIJ) pada tahun 2001. Pada tahun 2002, dengan ditambahnya tugas dan fungsi CEVEST untuk melaksanakan pelatihan kerja keluar negeri, maka lembaga pengembangan pelatihan yang merupakan unit kerja Eselon III ini berubah menjadi unit kerja Eselon II dengan nama Pusat Pelatihan Kerja Tenaga Kerja Luar Negeri (Puslatker TKLN).Pada tahun 2006, CEVEST diberi nama Balai Besar Pengembangan Latihan Kerja Luar Negeri (BBPLKLN), hingga akhirnya pada tahun 2017 berubah nama menjadi Balai Besar Pengembangan Latihan Kerja (BBPLK) hingga saat ini. 
